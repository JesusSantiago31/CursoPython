#Palabra clave end
#Conformada por la palabra clave end, un signo de igual, y un valor 
#Evita u salto de linea
print("Mi nombre es", "Python.", end=" ")
print("Monty Python.")

#Palabra clave sep, viene de separador
#Quita los espacios que da Python entre argumentos
#Conformada por la palabra clave sep, signo de igual, y el valor que puede sustituir el espacio
print("Mi", "nombre", "es", "Monty", "Python.", sep="-")

#Se pueden mezclar las palabras clave
print("Mi", "nombre", "es", sep="_", end="*")
print("Monty", "Python.", sep="*", end="*\n")

# Agregar elementos a una lista 
# Metodo append(): Agrega el elemento al final de la lista
# Metodo insert(): Agrega el elemento en cualquier lugar de la lista

numbers = [111, 7, 2, 1]
print(len(numbers))
print(numbers)

###

numbers.append(4)

print(len(numbers))
print(numbers)

###

numbers.insert(0, 222)
print(len(numbers))
print(numbers)

